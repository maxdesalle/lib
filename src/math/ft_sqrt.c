#include "libft.h"

double	ft_sqrt(double x)
{
	float	y;
	float	z;

	y = 0;
	z = x / 2;
	if (x > 0)
		while (y != z)
		{
			y = z;
			z = (x / y + y) / 2;
		}
	return (y);
}
